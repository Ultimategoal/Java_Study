package e_oop;

public class Calculator {
	
	double plus (double a, double b){
		return a + b;
	}
	double minus (double a, double b){
		return a - b;
	}
	double multiply (double a, double b){
		return a * b;
	}
	double devide (double a, double b){
		return a / b;
	}
	double rest (double a, double b){
		return a % b;
	}
}
